package com.java.TowerPower.Model;

import java.util.List;

public class Level {
	
	private BackgroundId backgroundId;
	private List<BackgroundId> enemyAndTileGirdList;
	
	public Level(){}
	
	public BackgroundId getBackgroundId() {
		return backgroundId;
	}

	public void setBackgroundId(BackgroundId backgroundId) {
		this.backgroundId = backgroundId;
	}

	public List<BackgroundId> getEnemyAndTileGirdList() {
		return enemyAndTileGirdList;
	}

	public void setEnemyAndTileGirdList(List<BackgroundId> enemyAndTileGirdList) {
		this.enemyAndTileGirdList = enemyAndTileGirdList;
	}
	
	public Level(BackgroundId backgroundId, List<BackgroundId> list) {
		this.backgroundId = backgroundId;
		this.enemyAndTileGirdList = list;
	}

}
